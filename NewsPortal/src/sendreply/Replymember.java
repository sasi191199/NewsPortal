package sendreply;

public class Replymember {
private String uname,message;
public Replymember()
{
	super();
}
public Replymember(String uname,  String message) {
	super();
	this.uname = uname;
	
	this.uname = uname;
	this.message = message;
	
}

public String getUname() {
	return uname;
}

public void setUname(String uname) {
	this.uname = uname;
}

public String getMessage() {
	return message;
}

public void setMessage(String message) {
	this.message = message;
}

}
